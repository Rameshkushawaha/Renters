    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add Room code:
        try {
            String room_no = r_no.getText();
            
            String ac;
            if (ac1.isSelected()) {
                ac = ac1.getText();
            } else {
                ac = ac2.getText();
            }
            String type = (String) t.getSelectedItem();
            
            int vacant = 0;
            if (type.equalsIgnoreCase("Single")) {
                vacant = 1;
            } else if (type.equalsIgnoreCase("Double")) {
                vacant = 2;
            } else {
                vacant = 3;
            }
            if(!room_no.equals("") && !p.getText().equals("") && !f.getText().equals("")){
                
                int price = Integer.parseInt(p.getText());
                int floor = Integer.parseInt(f.getText());
                
            db.DbConnect d = new db.DbConnect();
            db.DbConnect.st.executeUpdate("insert into rooms(room_no,price,ac,type,floor,vacant_bed) values('" + room_no + "'," + price + ",'" + ac + "','" + type + "'," + floor + "," + vacant + ")");
            getEntries();
            JOptionPane.showMessageDialog(null, "Room Added Successfully!");
            r_no.setText(null);
            p.setText(null);
            f.setText(null);
            }else{
               JOptionPane.showMessageDialog(null, "Please fill the complete form!");
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

