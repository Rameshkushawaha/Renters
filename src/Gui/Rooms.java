    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Delete room
        try {
            int ri = jTable1.getSelectedRow();
            if (ri != -1) {
                int r = JOptionPane.showConfirmDialog(null, "Do you really wanna Delete", "Confirmation", JOptionPane.YES_NO_OPTION);
                if (r == JOptionPane.YES_OPTION) {
                    String rn = (String) jTable1.getValueAt(ri, 1);
                   // db.DbConnect d = new db.DbConnect();
                    db.DbConnect.st.executeUpdate("delete from rooms where room_no='" + rn + "'");
                    getEntries();
                    JOptionPane.showMessageDialog(null, "room deleted successfully!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please Select any row!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

